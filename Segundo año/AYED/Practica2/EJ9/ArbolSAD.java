public class ArbolSAD {
    private int suma;
    private int dif;

    public ArbolSAD(int i, int j){
        this.suma = i;
        this.dif = j;
    }
    
    
    public int getSuma() {
        return suma;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }

    public int getDif() {
        return dif;
    }

    public void setDif(int dif) {
        this.dif = dif;
    }
}
